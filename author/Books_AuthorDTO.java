package com.author;

public class Books_AuthorDTO {
	
	private String authorId;
	private String authorName;
	private String authorBirth;
	private String authorPlace;
	private String authorDebut;
	private String debutBook;
	private String authorCategory;
	
	public String getAuthorId() {
		return authorId;
	}
	public void setAuthorId(String authorId) {
		this.authorId = authorId;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public String getAuthorBirth() {
		return authorBirth;
	}
	public void setAuthorBirth(String authorBirth) {
		this.authorBirth = authorBirth;
	}
	public String getAuthorPlace() {
		return authorPlace;
	}
	public void setAuthorPlace(String authorPlace) {
		this.authorPlace = authorPlace;
	}
	public String getAuthorDebut() {
		return authorDebut;
	}
	public void setAuthorDebut(String authorDebut) {
		this.authorDebut = authorDebut;
	}
	public String getDebutBook() {
		return debutBook;
	}
	public void setDebutBook(String debutBook) {
		this.debutBook = debutBook;
	}
	public String getAuthorCategory() {
		return authorCategory;
	}
	public void setAuthorCategory(String authorCategory) {
		this.authorCategory = authorCategory;
	}
	
}
